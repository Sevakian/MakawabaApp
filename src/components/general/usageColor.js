export function usageColor(x){
    switch(x){
        case 1:
            return "background-color:#00ff00;";
        case 2: 
            return "background-color:#F90;";
        case 3: 
            return "background-color:#F00;";
        case 4: 
            return "background-color:#630;";
        case 5: 
            return "background-color:#9FF;";
        case 6: 
            return "background-color:#FF0;";
        default:
            return "background-color:#00ff00"
    }
}

